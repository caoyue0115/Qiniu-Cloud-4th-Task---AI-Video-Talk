# ===== 语音意图：识别进入/退出模式的指令 =====

# 关键词命中即切换。退出优先级最高。
_ENTER = {
    "nav": ["导航模式", "开始导航", "进入导航", "带我走", "帮我导航"],
    "read": ["阅读模式", "朗读模式", "进入阅读", "帮我读这页", "读这一页", "阅读这个"],
    "chat": ["聊天模式", "进入聊天", "陪我聊", "陪我说话", "我们聊聊"],
}
_EXIT = ["退出模式", "退出导航", "退出阅读", "退出聊天", "返回问答",
         "普通模式", "问答模式", "结束模式", "退出", "返回"]


def detect_mode_command(text: str):
    """返回 'nav'|'read'|'chat'|'qa'（退出回到问答）或 None（非模式指令）。"""
    if not text:
        return None
    t = text.strip()
    # 退出词优先
    for kw in _EXIT:
        if kw in t:
            return "qa"
    for mode, kws in _ENTER.items():
        if any(kw in t for kw in kws):
            return mode
    return None
